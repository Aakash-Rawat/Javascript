// +++++++++++ Math Methods & Properties ++++++++++

//value of pi
console.log(Math.PI);

//The trunc() method removes all the decimal points
console.log(Math.trunc(Math.PI));

// round() method return the value to the nearest integer.
console.log(Math.round(Math.PI));

//ceil() method return the next value even if decimal value is less than 5
console.log(Math.ceil(3.2));

//floor() method will rerturn the shortest rounded integer even if decimal vlaue is above 5.
console.log(Math.floor(3.6));

// pow() method for doing power of any number
console.log(Math.pow(2,4));

// min && max method provides min and max values
console.log(Math.min(2,4,1));
console.log(Math.max(2,6,1));

//random method generates random number between 0 and 1
console.log(Math.random());
//To get integer value between 1 to 10 use floor or ceil
console.log(Math.floor(Math.random()*10)+1);








